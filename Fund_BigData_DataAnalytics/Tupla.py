# TUPLA
'''
Uma tupla em Python é uma estrutura de dados imutável
que permite armazenar uma coleção de itens. 
'''

'''
# Criando uma tupla com alguns itens
minha_tupla = (1, 2, 3, 4, 5)# ou minha_tupla = 1,2,3,4,5
print(minha_tupla)
'''

'''
# Imprimindo o tamanho da tupla
print(len(minha_tupla)) # Output: 5
'''

'''
# Acessando um item específico na tupla
print(minha_tupla[2]) # Output: 3
'''

'''
# Verificando se um item está na tupla
print(4 in minha_tupla) # Output: True
'''

'''
# Concatenando duas tuplas
outra_tupla = (6, 7, 8)
minha_tupla = minha_tupla + outra_tupla
'''

